#access tuples
tuple1 = ("apple", "orange", "pear")
print(tuple1[2])

#negative indexing - starts from the end of the list
tuple2 = (1, 2, 3, 4, 5, 6, 7, 8, 9)
print(tuple2[-1])

#range of negative indexes
thisTuple = ("apple", "orange", "pear")
#-4(included) to -1(not included)
print(thisTuple[-4:-1])

